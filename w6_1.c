#include <stdio.h>

int main() {

int a = 5, b =17 ; //กำหนดตัวแปล a = 5 b = 17

float c = 8.5 , d = 4.0 ; //กำหนดตัวแปล c = 8.5 d = 4.0

printf("a = %d, b = %d, c = %.2f, d = %.2f\n", a, b, c, d) ; //แสดงผลตัวแปล a b c d

printf("d + a = %.2f\n", d + a); //แสดงผลการบวก d + a

printf("a - b = %d\n", a - b); //แสดงผลการลบ a - b

printf("c * d = %.2f\n", c * d); //แสดงผลการคูณ c * d

printf("a * c = %.2f\n", a * c); //แสดงผลการคูณ a * c

printf("c / d = %.2f\n", c / d); //แสดงผลการหาร c / d

printf("b / c = %.2f\n", b / c); //แสดงผลการหาร b / c

printf("a %% b = %d\n", a % b); //แสดงผลการหารเอาเศษ a % b

printf("c %% a = %d\n", (int)c % a); //แสดงผลการหารเอาเศษ c % a

printf("c %% d = %.2f\n", (int)c % (int)d); //แสดงผลการหารเอาเศษ c % d

return 0; //คืนค่า 0 เพื่อบ่งบอกว่าโปรแกรมทำงานสำเร็จ

}

// เมื่อรันโปรแกรมจะแสดงผลลัพธ์ดังนี้:
// a = 5, b = 17, c = 8.50, d = 4.00
// d + a = 4.00 + 5 = 9.00
// a - b = 5 - 17 = -12
// c * d = 8.50 * 4.00 = 34.00
// a * c = 5 * 8.50 = 42.50
// c / d = 8.50 / 4.00 = 2.12
// b / c = 17 / 8.50 = 2.00
// a % b = 5 % 17 = 5
// c % a = 8.50 % 5 = 3
// c % d = 8.50 % 4.00 = 0.00
// หมายเหตุ: การใช้ % กับตัวแปร float จะต้องแปลงเป็น int ก่อน เนื่องจาก % เป็นโอเปอเรเตอร์สำหรับจำนวนเต็มเท่านั้น
// และการแสดงผลของ float จะใช้ %.2f เพื่อแสดงทศนิยม 2 ตำแหน่ง

