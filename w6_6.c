#include <stdio.h>

int main() {
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;

    // r1: a++ * b + (int)y % 3
    // a++ = 5 (ใช้ค่าเดิมก่อนเพิ่ม), b = 2, (int)y = 4, 4 % 3 = 1
    // r1 = 5*2 + 1 = 10 + 1 = 11, a กลายเป็น 6
    int r1 = a++ * b + (int)y % 3;
    printf("r1 = a++ * b + (int)y %% 3;             r1 = %d\n", r1);

    // r2: (a > b) && ((int)x / b < 2)
    // a = 6, b = 2, x = 3.0, (int)x = 3, 3/2 = 1 < 2 เป็นจริง
    // (6 > 2) เป็นจริง && (1 < 2) เป็นจริง => r2 = 1
    int r2 = (a > b) && ((int)x / b < 2);
    printf("r2 = (a > b) && ((int)x / b < 2);      r2 = %d\n", r2);

    // r3: ++x * y - a / 2
    // ++x = 4.0, y = 4.5, a = 6, 6/2 = 3
    // r3 = 4.0 * 4.5 - 3 = 18.0 - 3 = 15.0
    float r3 = ++x * y - a / 2;
    printf("r3 = ++x * y - a / 2;                  r3 = %.2f\n", r3);

    // r4: ((x += 1.5) > y) || (b-- > 0)
    // x = 4.0 + 1.5 = 5.5, y = 4.5, 5.5 > 4.5 เป็นจริง => r4 = 1.0, b กลายเป็น 1
    float r4 = ((x += 1.5) > y) || (b-- > 0);
    printf("r4 = ((x += 1.5) > y) || (b-- > 0);    r4 = %.2f\n", r4);

    return 0;
}

/*
อภิปรายผล:
- r1 ใช้ a++ จึงนำค่าเดิมของ a มาคำนวณก่อนแล้วจึงเพิ่มค่า a
- r2 เป็นนิพจน์ตรรกะ ผลลัพธ์เป็น 1 (จริง) หรือ 0 (เท็จ)
- r3 ใช้ ++x ทำให้ค่า x เพิ่มก่อนนำไปคูณ
- r4 ใช้ x += 1.5 และ b-- ผลลัพธ์เป็นจริง (1.0) เพราะเงื่อนไขแรกเป็นจริง
- ผลลัพธ์ float แสดงทศนิยม 2 ตำแหน่ง
*/